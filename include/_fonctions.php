///////////////////////////////////////////////////////////////////////////////////////
// ************************** FONCTIONS MEMBRE CONNECTÉ *****************************//
///////////////////////////////////////////////////////////////////////////////////////
function isConnect(): bool
{
    if(!isset($_SESSION['user'])) {
        return false;
    }

    return true;
}

//////////////////////////////////////////////////////////////////////////////////////
// ****************************** FONCTIONS ADMIN **********************************//
//////////////////////////////////////////////////////////////////////////////////////
///
function isAdminConnect(): bool
{

    return isConnect() && $_SESSION['user']['statut'] === '1';

}

